package com.techmeskills.homework_8.doctor;

public class Patient {

    //    to create field of class
    private int planTreatment;
    private String doctor;

    //    to create constructor class
    public Patient(int planTreatment) {
        this.planTreatment = planTreatment;
        this.doctor = doctor;
    }

//   to create method to get and to set
    public void setPlanTreatment(int planTreatment) {
        this.planTreatment = planTreatment;
    }

    public int getPlanTreatment() {
        return planTreatment;
    }

}
